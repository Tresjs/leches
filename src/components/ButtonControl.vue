}

.leches-btn-block {
  @apply px-4 py-2 text-base w-full justify-center;
}

.leches-btn-primary {
  @apply bg-gray-100 text-gray-700 dark:bg-dark-400 dark:text-gray-400;
}
.leches-btn-primary:hover {
  @apply bg-gray-200 dark:bg-dark-200;
}

.leches-btn-secondary {
